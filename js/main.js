// Old JQuery converted to vanilla js
window.addEventListener('load', function(){
	var path = window.location.pathname;
	var page = path.split("/").pop();

	var mbt1 = document.getElementsByClassName('mtb-1')[0];
	var mbt2 = document.getElementsByClassName('mtb-2')[0];
	var status = document.getElementsByClassName('market-status')[0];
	var list = document.getElementsByClassName('market-list')[0];
	var stack = document.getElementsByClassName('my_stack')[0];
	var graph = document.getElementsByClassName('graph-panel')[0];
	var container = document.getElementsByClassName('market-item-container')[0];

	// Button 1

	function button1Event() {
		removeClass(graph, 'graph-panel-show');
		show(status);
		show(list);
		show(container);
		if( page == "wire3.html"){
			show(stack);
		}
		removeClass(mbt2,'mobile-toggle-selected');
		addClass(mbt1,'mobile-toggle-selected');
	}
	if(mbt1) {
		mbt1.addEventListener("click", button1Event);
	}


	// Button 2
	if(mbt2) {

		mbt2.addEventListener("click", function() {
			hide(status);
			hide(list);
			hide(container);
			if( page == "wire3.html"){
				hide(stack);
			}
			removeClass(mbt1,'mobile-toggle-selected');
			addClass(mbt2,'mobile-toggle-selected');
			addClass(graph,'graph-panel-show');
		});

	}

	// Resize back to fullscreen show everything
	window.addEventListener("resize", function(){
		if (this.innerWidth > 999){
			button1Event();
		}
	});

	// Force refresh of elements with class name reloader
	// to get around anchor that dont reload
	var reloader = function () {
		document.location.reload(true);
	}
	var reloadArray = document.getElementsByClassName('reloader');
	//console.log(reloadArray.length);
	for( var i = 0; i<reloadArray.length; i++){
		reloadArray[i].addEventListener('click', reloader);
	}

	//Logout Button
	var logoutButton = document.getElementById('logout');
	if(logoutButton){
		logoutButton.addEventListener('click', function(){
			logout();
		});
	}


});

function hide(el){
	if(el){
		el.style.display = 'none';
	}
}
function show(el){
	if(el){
		el.style.display = '';
	}
}
function addClass(el, cName){
	if(el){
		el.className = el.className + ' ' + cName;
	}
}

function removeClass(el, cName){
	if(el){
		//console.log('Removed');
		el.className = el.className.replace(new RegExp(" ?\\b"+cName+"\\b"),'');
	}
}
function logout(){
	cookie.set('cacheit_sessionToken', null);
	window.location.href = "index.html";
}

